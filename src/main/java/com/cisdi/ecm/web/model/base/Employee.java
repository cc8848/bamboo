package com.cisdi.ecm.web.model.base;

public class Employee {
	private int id;
	private String empNum;
	private String empName;
	private String emailAddres;
	private String empEnName;
	private int deptId;
	private String deptName;
	private String stauts;
	private String empSpecialtyCode;
	private String empSpecialtyName;
	private String officeTel;
	private String cellTel;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEmpNum() {
		return empNum;
	}
	public void setEmpNum(String empNum) {
		this.empNum = empNum;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmailAddres() {
		return emailAddres;
	}
	public void setEmailAddres(String emailAddres) {
		this.emailAddres = emailAddres;
	}
	public String getEmpEnName() {
		return empEnName;
	}
	public void setEmpEnName(String empEnName) {
		this.empEnName = empEnName;
	}
	public int getDeptId() {
		return deptId;
	}
	public void setDeptId(int deptId) {
		this.deptId = deptId;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public String getStauts() {
		return stauts;
	}
	public void setStauts(String stauts) {
		this.stauts = stauts;
	}
	public String getEmpSpecialtyCode() {
		return empSpecialtyCode;
	}
	public void setEmpSpecialtyCode(String empSpecialtyCode) {
		this.empSpecialtyCode = empSpecialtyCode;
	}
	public String getEmpSpecialtyName() {
		return empSpecialtyName;
	}
	public void setEmpSpecialtyName(String empSpecialtyName) {
		this.empSpecialtyName = empSpecialtyName;
	}
	public String getOfficeTel() {
		return officeTel;
	}
	public void setOfficeTel(String officeTel) {
		this.officeTel = officeTel;
	}
	public String getCellTel() {
		return cellTel;
	}
	public void setCellTel(String cellTel) {
		this.cellTel = cellTel;
	}
	
	
}
